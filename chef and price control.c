#include <stdio.h>

int main()
{
    int t;
    scanf("%d",&t);
    while(t--)
    {
        int n,k,a[100000],i,d=0;
        scanf("%d%d",&n,&k);
        for(i=0;i<n;i++)
        scanf("%d",&a[i]); 
        for(i=0;i<n;i++)
        if(a[i]>k)
        {
        d=d+(a[i]-k);
        }
        printf("\n%d",d);
    }
    return 0;
}
        
